CREATE TABLE TB_USER (
    PK_USER             BIGINT(20)    PRIMARY KEY,
    TX_NAME             VARCHAR(255)  NOT NULL,
    TX_EMAIL            VARCHAR(50)   NOT NULL,
    TX_PASSWORD         VARCHAR(150)  NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE TB_PERMISSION (
    PK_PERMISSION        BIGINT(20) PRIMARY KEY,
    TX_DESCRIPTION       VARCHAR(50) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8;


CREATE TABLE TB_USER_PERMISSION (
    FK_USER       BIGINT(20) NOT NULL,
    FK_PERMISSION BIGINT(20) NOT NULL,
    PRIMARY KEY (FK_USER, FK_PERMISSION),
    FOREIGN KEY (FK_USER) REFERENCES TB_USER(PK_USER),
    FOREIGN KEY (FK_PERMISSION) REFERENCES TB_PERMISSION(PK_PERMISSION)

) ENGINE=InnoDB DEFAULT CHARSET=utf8;

